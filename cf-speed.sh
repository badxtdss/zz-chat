  172.67.0.1 172.67.1.1 172.67.2.1 172.67.3.1
)

RESULTS=""

for ip in "${IPS[@]}"; do
  t=$(curl -so /dev/null -w "%{time_connect}" --connect-timeout $TIMEOUT --resolve "$DOMAIN:443:$ip" "https://$DOMAIN/" 2>/dev/null)
  if [ -n "$t" ] && [ "$t" != "0.000" ]; then
    ms=$(echo "$t * 1000" | bc 2>/dev/null || echo "${t}s")
    RESULTS="${RESULTS}${ms} ${ip}\n"
    printf "."
  fi
done

echo ""
echo ""
echo "=== TOP ${TOP_N} ==="
echo -e "$RESULTS" | sort -n | head -n $TOP_N

echo ""
echo "最快IP: $(echo -e "$RESULTS" | sort -n | head -1 | awk '{print $2}')"
echo ""
echo "去CF控制台 → $DOMAIN → DNS → A记录 → 改成上面的最快IP"
