TIMEOUT=3
TOP_N=5

echo "=== CF优选IP测速 ==="
echo "域名: $DOMAIN"
echo ""

# CF常见IP段
IPS=(
  104.16.0.1 104.16.1.1 104.16.2.1 104.16.3.1 104.16.4.1 104.16.5.1 104.16.6.1 104.16.7.1
  104.17.0.1 104.17.1.1 104.17.2.1 104.17.3.1 104.17.4.1 104.17.5.1
  104.18.0.1 104.18.1.1 104.18.2.1 104.18.3.1 104.18.4.1 104.18.5.1
  104.19.0.1 104.19.1.1 104.19.2.1 104.19.3.1
  104.20.0.1 104.20.1.1 104.20.2.1 104.20.3.1 104.20.4.1 104.20.5.1 104.20.6.1 104.20.7.1
  104.21.0.1 104.21.1.1 104.21.2.1 104.21.3.1 104.21.4.1 104.21.5.1 104.21.6.1 104.21.7.1
  104.21.8.1 104.21.16.1 104.21.32.1 104.21.48.1 104.21.64.1 104.21.80.1 104.21.96.1 104.21.112.1
  104.21.128.1 104.21.160.1 104.21.192.1 104.21.224.1
  104.22.0.1 104.22.1.1 104.22.2.1 104.22.3.1
  104.23.0.1 104.23.1.1 104.23.2.1 104.23.3.1
